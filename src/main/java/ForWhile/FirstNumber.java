package ForWhile;

public class FirstNumber {
    //Вывести 10 первых чисел последовательности 0, -5,-10,-15..
    public static void main(String[] args) {
        int i = 0;
        while(i >= -45){
            System.out.print(i+ " ");
            i = i-5;
        }
        System.out.println();
    }
}
